# Definition for singly-linked list.
class ListNode:
    def __init__(self, x):
        self.val = x
        self.next = None


class Solution:
    def reverseKGroup(self, head: ListNode, k: int) -> ListNode:
        count = 0
        f = head
        while head and count < k:
            head = head.next
            count += 1

        if count == k:
            p = self.reverseKGroup(head, k)
            for _ in range(count):
                n = f.next
                f.next = p
                p = f
                f = n
            return p
        return f


